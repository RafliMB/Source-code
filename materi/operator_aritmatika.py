# Operasi Aritmatika
a = 5
b = 10
c = 6

penjumlahan = a + b
print(f'Hasil Penjumlahan : {penjumlahan}')

pengurangan = a - b
print(f'Hasil Pengurangan : {pengurangan}')

perkalian = a * b
print(f'Hasil Perkalian : {perkalian}')

pembagian = a / b
print(f'Hasil Pembagian : {pembagian}')

modulus = c % a
print(f'Hasil Sisa : {modulus}')

pemangkatan = b ** a
print(f'10 pangkat 5 : {pemangkatan}')